package com.example.androidstudioproject_tomroche.dao;

import androidx.room.Dao;
import androidx.room.Delete;
import androidx.room.Insert;
import androidx.room.Query;
import androidx.room.Update;

import com.example.androidstudioproject_tomroche.entities.Instructor;

import java.util.List;

@Dao
public interface InstructorDAO {
    @Insert(entity = Instructor.class)
    void insertAll(Instructor... instructors);

    @Delete(entity = Instructor.class)
    void delete(Instructor instructor);

    @Query("select * from instructors")
    List<Instructor> getAll();

    @Update(entity = Instructor.class)
    void update(Instructor... instructor);
}




































































































































































































































































































































































































































































































































































































































































































































































































































